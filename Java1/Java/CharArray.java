class CharArray
{
	public static void main(String k[])
	{
		
		char name[]={'k','a','r','t','h','i'};
		int i;
		
		for(i=0;i<name.length;i++)
		{
			System.out.println(name[i]);
		}
		
		System.out.println("\nReverse of String");

		for(i=name.length-1;i>=0;i--)  //for(i=5;i>=0;i++)

		{
			System.out.println(name[i]);
		}
	}
}
		
		
